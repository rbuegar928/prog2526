package ejerciciosProgUD5.ejercicio13;

public final class Operaciones {
	
	double a;
	double b;
	
	public static final double Sumar(double a,  double b){
		return a + b;
	}
	public static final double Restar(double a,  double b){
		return a - b;
	}
	public static final double Multiplicar(double a,  double b){
		return a * b;
	}
	public static final double Dividir(double a,  double b){
		return a / b;
	}
	
}
